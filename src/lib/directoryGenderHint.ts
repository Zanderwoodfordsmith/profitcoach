/** Returns true when the first name is one we are fairly confident is female. */
export function likelyFemaleFirstName(fullName: string | null | undefined): boolean {
  if (!fullName) return false;
  const first = fullName
    .trim()
    .split(/\s+/)[0]
    ?.toLowerCase()
    .replace(/[^a-z]/g, "");
  if (!first) return false;
  return FEMALE_FIRST_NAMES.has(first);
}
